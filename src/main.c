void renderFunction(void){
	glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);

	glLoadIdentity();
	glRotatef(angle_h, 0, 1, 0);
	glRotatef(angle_v, 1, 0, 0);
	
	// Label iso
	char ch[20];
	sprintf(ch, "%.2f", iso);
	glRasterPos3f(display_size+0.15, display_size+0.15,display_size+0.15);
	glutBitmapString(GLUT_BITMAP_TIMES_ROMAN_24, ch);

	// each value has a dot and text
	
	for(int i = 0 ; i < size*size*size; i++){
		if (display_mode > 0){
			glBegin(GL_POINTS);
			glVertex3f(dataArray[i].x, dataArray[i].y, dataArray[i].z);
			glEnd();
		}
		if (display_number){
			glRasterPos3f(dataArray[i].x, dataArray[i].y, dataArray[i].z);
			sprintf(ch, "%.2f", dataArray[i].val);
			glutBitmapString(GLUT_BITMAP_TIMES_ROMAN_10, ch);
		}
	}
	
	// Cube extérieur (x y z)
	if (display_mode > 1){
		Point3 po;
		po.x = display_size;
		po.y = display_size;
		po.z = display_size;
		drawCubeFrom(po, display_size*2);
	}
	
	// Cubes intérieur
	double step = (display_size*2) / (size-1);
	if (display_mode > 2){
		for (int i = 1 ; i < size ; i++){
			for (int j = 1 ; j < size ; j++){
				for (int k = 1 ; k < size ; k++){
					int index = i*size*size+j*size+k ;
					drawCubeFrom(dataArray[index], step);
	 			}
			}
		}
	}

	for (int i = 0; i < size-1 ;i ++){
		for (int j = 0; j < size-1 ; j++){
			for (int k = 0; k < size-1 ; k++){
				int c_index = i*(size-1)*(size-1) + j*(size-1) + k;
				int *lst_tri = triangleArray[indexCube(cubeArray[c_index], iso)];
				int cursor = 0;
				while (cursor < 15 && lst_tri[cursor] != -1){
					Point3 pts[3];
					for(int l=0; l<3 ; l++){
						int *corners = linkedCorner[lst_tri[cursor+l]];
						interpolatePos3(
							&pts[l],
							cubeArray[c_index].corner[corners[0]],
							cubeArray[c_index].corner[corners[1]],
							iso
							);
					}
					drawTriangle(pts);
					cursor +=3;
				}
			}
		}
	}
	

	glutSwapBuffers();
	glutPostRedisplay();
}

void keyboard(unsigned char key, int x, int y){
	switch(key){
		case 'n':
			display_number = (display_number+1)%2;
			break;
		case 'f':
			display_filled = (display_filled+1)%2;
			break;
		case '+':
			iso += 0.01;
			break;
		case '-':
			iso -= 0.01;
			break;
		case 32: // espace
			display_mode = (display_mode+1)%MAX_MODE;
			break;
		case 27: // Esc
			exit(1);
		//default:
			//printf("%d\n",key);
	}
	glutPostRedisplay();
}

void passiveMotion(int x, int y){
	prev_x = x;
	prev_y = y;
}

void motion(int x, int y){
	if (prev_x!=-1){
		angle_h += x-prev_x;
		glutPostRedisplay();
	}

	if (prev_y!=-1){
		angle_v += y-prev_y;
		glutPostRedisplay();
	}
	prev_x = x;
	prev_y = y;
}

