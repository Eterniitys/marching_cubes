void interpolatePos3(Point3 *p, Point3 p1, Point3 p2, double iso){

	if (abs(iso-p1.val) < 0.00001){
		p->x = p1.x;
		p->y = p1.y;
		p->z = p1.z;
	}
	if (abs(iso-p2.val) < 0.00001){
		p->x = p2.x;
		p->y = p2.y;
		p->z = p2.z;
	}
	if (abs(p1.val-p2.val) < 0.00001){
		p->x = p1.x;
		p->y = p1.y;
		p->z = p1.z;
	}

	p->x = p1.x + ((iso-p1.val)/(p2.val-p1.val)) * (p2.x-p1.x);
	p->y = p1.y + ((iso-p1.val)/(p2.val-p1.val)) * (p2.y-p1.y);
	p->z = p1.z + ((iso-p1.val)/(p2.val-p1.val)) * (p2.z-p1.z);
}

// drawing functions

int indexCube(Cube c, double iso){	
	int i = 0;
	i += c.corner[0].val < iso ? 1   : 0;
	i += c.corner[1].val < iso ? 2   : 0;
	i += c.corner[2].val < iso ? 4   : 0;
	i += c.corner[3].val < iso ? 8   : 0;
	i += c.corner[4].val < iso ? 16  : 0;
	i += c.corner[5].val < iso ? 32  : 0;
	i += c.corner[6].val < iso ? 64  : 0;
	i += c.corner[7].val < iso ? 128 : 0;
	return i;
}

void drawTriangle(Point3 *pts){
	glBegin(display_filled ? GL_TRIANGLES : GL_LINE_LOOP);  //
	glVertex3f(pts[0].x, pts[0].y, pts[0].z);
	glVertex3f(pts[1].x, pts[1].y, pts[1].z); 
	glVertex3f(pts[2].x, pts[2].y, pts[2].z);
	glEnd();
}

void drawCubeFrom(Point3 p, double size){
	glBegin(GL_LINE_LOOP);  //
	p.x = -p.x;
	//face avant
	glVertex3f(p.x, p.y, p.z);
	glVertex3f(p.x+size, p.y, p.z); 
	glVertex3f(p.x+size, p.y-size, p.z);
	glVertex3f(p.x, p.y-size, p.z);
	glEnd();
	//face arriére
	glBegin(GL_LINE_LOOP);
	glVertex3f(p.x, p.y, p.z-size);
	glVertex3f(p.x+size, p.y, p.z-size); 
	glVertex3f(p.x+size, p.y-size, p.z-size);
	glVertex3f(p.x, p.y-size, p.z-size);
	glEnd();
	//fermeture du cube
	glBegin(GL_LINES);  // x y z
	glVertex3f( p.x, p.y, p.z);
	glVertex3f( p.x, p.y, p.z-size);
	//
	glVertex3f( p.x+size, p.y, p.z);
	glVertex3f( p.x+size, p.y, p.z-size);
	//
	glVertex3f( p.x, p.y-size, p.z);
	glVertex3f( p.x, p.y-size, p.z-size);
	//
	glVertex3f( p.x+size, p.y-size, p.z);
	glVertex3f( p.x+size, p.y-size, p.z-size);
	glEnd();
}

